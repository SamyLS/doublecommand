2005-08-18<br />
I am very pleased to finally release DoubleCommand for Mac OS 10.4 (Tiger). The new event queue turned out to be a dead end, but thanks to the technique used by <a href="http://www.kodachi.com/fKeys/">fKeys</a> which I have shamelessly copied, Tiger support is here.</p>
<p>
2005-07-29<br />
Thanks for the many messages of support for DoubleCommand. I've tracked down the changes in 10.4, which is using a new keyboard event queue system in place of the command gate system. I'm currently experimenting with ways to hook into the new mechanism.</p>
<p>
2005-06-24<br />
The order is in for Mac OS 10.4, so work can begin once the package arrives. Thanks again to those who have donated!</p>
<p>
2005-05-27<br />
The requests for a DoubleCommand upgrade to work with Mac OS 10.4 keep coming in, all I can say is that I will be getting hold of Mac OS 10.4 in June, and I'll get DoubleCommand working as soon as I can.</p>
<p>
2005-05-02<br />
Over the weekend I have been sent many emails informing me that DoubleCommand does not work with Mac OS X 10.4 (Tiger). At this stage I don't know what will be involved in updating, and so I can't give any idea of when DoubleCommand will be ready.</p>
<p>
2005-04-12<br />
DoubleCommand 1.5.4 released for Mac OS X 10.3, added support for Enter as Forward Delete, and support for the new PowerBook's enter key.</p>
<p>
2004-12-17<br />
DoubleCommand 1.5.3 released for Mac OS X 10.3, added support for Control + Home/End PC style and fixed a bug with Command -> Option mapping.</p>
<p>
2004-08-10<br />
DoubleCommand 1.5.2 released for Mac OS X 10.3, this release enables right side modifier keys under 10.3. Wait, right side keys? Yes, under 10.3 (I'm not sure which version since, but 10.3.4 at least) DoubleCommand can tell between the different modifier keys, so some requests for different behaviour based on which side is pressed will be fulfilled in the near future.</p>
<p>
2004-07-16<br />
DoubleCommand 1.5.1 released for Mac OS X 10.3, including a new Preference pane and a standard install package.</p>
<p>
DoubleCommand 1.5 released for Mac OS X 10.3 (Panther). I've realised that the install script requires the development tools, so I'll be switching to a Mac OS X package format for the next release. I also have a preference pane contributed by Christian Nadolle - it will be uploaded soon and feature in the package when that arrives.
</p>
<p>
DoubleCommand 1.5b3 released for Mac OS X 10.3 (Panther). Fixes the shift delete as forward delete feature, and adds kernel panic protection. If DoubleCommand fails to load, it will disable itself to avoid further panics.
</p>
<p>
DoubleCommand 1.4.4 released for Mac OS X 10.2 only. DoubleCommand is not yet compatible with Panther (Mac OS X 10.3). I will be working on this and will update this site when a compatible version is available.
