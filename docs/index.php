<h2>News</h2>

<p>
27th October 2013 - Thanks to the efforts of [Adam Strzelecki](https://github.com/nanoant/doublecommand) DoubleCommand 1.7 is released, adding support for Mac OS X 10.9 (Mavericks), and dropping support for 10.4 and 10.5.
</p>

<p>
29th June 2011 - It has been a long time since anything has changed here, and so I'd just like to make you aware that if DoubleCommand does not fully support a new keyboard you use (like in a new Macbook perhaps), or if it doesn't support a feature you need, you might find some help at <a href="http://pqrs.org/macosx/keyremap4macbook/">pqrs.org/macosx/keyremap4macbook</a>.
</p>

<p>
24th November 2009 - DoubleCommand 1.6.9 released, fully supporting 64-bit for the prefpane and the kext (for those who want to use the 64-bit kernel). This is a universal binary, so only one kext and prefpane is needed for any Mac OS X 10.4 and up computer.
</p>

<p>
2nd September 2009 - Snow Leopard (Mac OS X 10.6) has been released, but I might not be able to have a copy running for a couple of weeks. However, most reports coming in are that the existing DoubleCommand works fine on 10.6 in 32 bit kernel mode, and if you're using DoubleCommand, chances are that 32 bit is not only the default, but is just fine for you right now. I have been sent a patch by Ryan Walklin (thanks!) that should support the 64 bit kernel, but I won't be able to release that until I can test that it works, which should be a couple of weeks at most.
</p>

<p>
10th March 2009 - DoubleCommand 1.6.8 just adds back the missing options from the preference pane.
</p>

<p>
5th February 2009 - Release DoubleCommand 1.6.7 is out, this fixes problems with the control key not working in certain situations, and adds an uninstaller script for when things go wrong.
</p>

<p>
9th May 2008 - New beta <a href="files/DoubleCommand-1.6.6b4.dmg">DoubleCommand 1.6.6b4</a> is out, this rolls back the control key fix from the last beta, which seemed to break some features, and adds a number of new features contributed by Sastira - Using the right Option key as forward delete, Control or Enter, swapping delete and forward delete, and using caps lock as forward delete.
</p>

<p>
25th March 2008 - New beta <a href="files/DoubleCommand-1.6.6b3.dmg">DoubleCommand 1.6.6b3</a> is out, this seems to fix kernel panics for some on Mac OS X 10.5, and should also solve a problem with the Control key becoming stuck. Please report whether it works for you or not, and what version of Mac OS you have, and the keyboards and type of computer you're using it on.
</p>

<p>
22nd February 2008 - I've just had a report that Mac OS X 10.5.2 seems to stop kernel panics when using DoubleCommand, so please post in the <a href="https://sourceforge.net/forum/forum.php?thread_id=1947193&forum_id=221238">forum</a> and let us all know your experience with this. I've also been told that there is a new Built-In Keyboard Firmware Update - please disable DoubleCommand before applying this update, you can safely add DoubleCommand back afterwards.
</p>

<p>
6th November 2007 - <a href="files/DoubleCommand-1.6.6b1.dmg">DoubleCommand 1.6.6b1</a> is available for testing, several reports of success on Mac OS X 10.5 have given me enough reason to post this here, however I have not yet tested this myself with the new OS. Please let me know how it works for you if you try it!
</p>

<p>
30th October 2007 - Mac OS X 10.5, Leopard, has been released, and I expect to have a copy at some point. Until then, reports have been mixed: some have had success running the current version of DoubleCommand as is, some seem to have required uninstalling then reinstalling, and some have experienced occasional kernel panics, only possibly related to DoubleCommand. My advice is to disable DoubleCommand before upgrading to avoid upgrade problems, then enable when you've got everything else sorted out.
</p>

<p>
6th August 2007 - Release 1.6.5 removes the unnecessary log output.
</p>

<p>
25th July 2007 - Release 1.6.4 adds Caps lock to delete feature for the
Colemak keyboard layout. Also added internals for multiple keyboard support.
</p>

<p>
26th June 2007 - Updated website layout. The highest priority now is having settings per keyboard, or at least only applying DoubleCommand to a single keyboard. Also released DoubleCommand 1.6.3, a minor update which allows using backslash as delete with Option to delete to word boundaries.
</p>


