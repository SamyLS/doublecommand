<h1>What is DoubleCommand?</h1>
<p>DoubleCommand is software for Mac OS X (a kernel extension) that lets you
remap keys, in other words change the way your keyboard works. Often used to
make a PC keyboard more comfortable with a Mac, swapping the Alt (Option) and
Windows (Command or Apple) keys, since they are in swapped positions on Mac
and PC keyboards. DoubleCommand is a quick and easy way to fix this if you
use a PC keyboard with your Mac.
</p>
